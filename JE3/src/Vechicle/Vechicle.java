package Vechicle;

public class Vechicle {
    private int price;
    private int speed;
    private int year;

    public Vechicle(){
        this.price = 150000;
        this.speed = 300;
        this.year = 2010;
    }

    public int getPrice() {
        return price;
    }

    public int getSpeed() {
        return speed;
    }

    public int getYear() {
        return year;
    }

}