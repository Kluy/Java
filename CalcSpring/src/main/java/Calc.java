public class Calc {
    private int a;
    private int b;

    public int plus(int a, int b){
        return a + b;
    }
    public int pow(int a, int b){
        return a * b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
